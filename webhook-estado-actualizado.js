/**
 * Funci√≥n auxiliar para enviar mensaje por WhatsApp
 */
async function enviarMensajeWhatsApp(numero, mensaje) {
    try {
        // Aqu√≠ va la integraci√≥n con Twilio o el servicio que uses
        const accountSid = process.env.TWILIO_ACCOUNT_SID;
        const authToken = process.env.TWILIO_AUTH_TOKEN;
        const client = require('twilio')(accountSid, authToken);
        
        await client.messages.create({
            body: mensaje,
            from: process.env.TWILIO_WHATSAPP_NUMBER,
            to: numero.startsWith('whatsapp:') ? numero : `whatsapp:${numero}`
        });
        
        console.log(`üì§ Mensaje enviado a ${numero}`);
        
    } catch (error) {
        console.error('‚ùå Error enviando WhatsApp:', error);
        throw error;
    }
}

